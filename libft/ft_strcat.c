#include "libft.h"

char		*ft_strcat(char *dest, const char *src)
{
	char	*dest2;

	dest2 = dest;
	while (*dest)
		dest++;
	while (*src)
	{
		*dest = *src;
		src++;
		dest++;
	}
	*dest = '\0';
	return (dest2);
}
